<template>
  <section class="projetos">
    <h1 class="title">Projetos</h1>
    <form @submit.prevent="salvar">
      <div class="field">
        <label
          for="nomeDoProjeto"
          class="label"
        >Nome do projeto</label>
        <input
          type="text"
          class="input"
          id="nomeDoProjeto"
          v-model="nomeDoProjeto"
        />
      </div>
      <div class="field">
        <button
          class="button"
          type="submit"
        >Salvar</button>
      </div>
    </form>
    <table class="table is-fullwidth mt-5">
      <thead>
        <tr>
          <th>ID</th>
          <th>Nome</th>
        </tr>
      </thead>
      <tbody>
        <tr
          v-for="projeto in projetos"
          :key="projeto.id"
        >
          <td>{{ projeto.id }}</td>
          <td>{{ projeto.nome }}</td>
        </tr>
      </tbody>
    </table>
  </section>
</template>
<script lang="ts">
import { defineComponent } from "vue";
import IProjeto from "./../interfaces/IProjeto";

export default defineComponent({
  name: "Projetos",
  data() {
    return {
      nomeDoProjeto: "",
      projetos: [] as IProjeto[],
    };
  },
  methods: {
    salvar() {
      const projeto: IProjeto = {
        id: new Date().toISOString(),
        nome: this.nomeDoProjeto,
      };
      this.projetos.push(projeto);
      this.nomeDoProjeto = "";
    },
  },
});
</script>

<style scoped>
.projetos {
  padding: 1.25rem;
}
</style>